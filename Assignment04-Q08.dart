// Implement a function that checks if a given string is a palindrome.
// Example:
// Input: "radar"
// Output: "radar" is a palindrome.
// Write a program to display the cube of the number up to an integer.
void main() {
  int n = 7;
  for (int i = 1; i <= n; i++) {
    int cube = i * i * i;
    print('Cube of $i: $cube');
  }
}
